//Declaring 4 functions

function add(a,b){
    return a+b;
}
function sub(a,b){
    return a-b;
}
function multiply(a,b){
    return a*b;
}
function divide(a,b){
    return a/b;
}

module.exports.add=add;
module.exports.sub=sub;
module.exports.multiply=multiply;
module.exports.divide=divide;